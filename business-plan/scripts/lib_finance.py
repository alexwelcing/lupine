"""Pure-Python finance functions used by analyze.py.

No openpyxl, no Excel, no external deps beyond the standard library.
Every function is deterministic and side-effect-free so the analysis
report can be regenerated by re-running analyze.py against the CSVs
in ../value-model/.

Conventions:
- All cash flows in $M unless explicitly noted
- WACC, terminal growth, tax rates as decimals (0.121 not 12.1)
- Mid-year discount convention by default
"""
from __future__ import annotations

from dataclasses import dataclass
from statistics import median
from typing import Iterable


# ---------------------------------------------------------------------------
# DCF
# ---------------------------------------------------------------------------

@dataclass
class DCFResult:
    sum_pv_fcfs: float
    pv_terminal: float
    enterprise_value: float
    equity_value: float
    terminal_value_undiscounted: float
    terminal_pct_of_ev: float
    pv_fcfs: list[float]
    discount_factors: list[float]


def dcf(
    fcfs: list[float],
    wacc: float,
    terminal_growth: float,
    *,
    net_debt: float = 0.0,
    mid_year: bool = True,
) -> DCFResult:
    """Standard Gordon-growth DCF with mid-year convention.

    Args:
        fcfs: list of unlevered free cash flows by projection year.
        wacc: discount rate (decimal).
        terminal_growth: perpetuity growth rate (decimal); must be < wacc.
        net_debt: subtracted from enterprise value to get equity value.
        mid_year: if True, discount periods are 0.5, 1.5, ...; else 1, 2, ...
    """
    if terminal_growth >= wacc:
        raise ValueError(
            f"terminal_growth ({terminal_growth}) must be < wacc ({wacc}) "
            "to avoid infinite terminal value"
        )
    n = len(fcfs)
    periods = [(i + (0.5 if mid_year else 1.0)) for i in range(n)]
    discount_factors = [1.0 / (1.0 + wacc) ** p for p in periods]
    pv_fcfs = [f * df for f, df in zip(fcfs, discount_factors)]
    sum_pv = sum(pv_fcfs)

    final_fcf = fcfs[-1]
    tv = final_fcf * (1 + terminal_growth) / (wacc - terminal_growth)
    pv_tv = tv * discount_factors[-1]
    ev = sum_pv + pv_tv
    return DCFResult(
        sum_pv_fcfs=sum_pv,
        pv_terminal=pv_tv,
        enterprise_value=ev,
        equity_value=ev - net_debt,
        terminal_value_undiscounted=tv,
        terminal_pct_of_ev=pv_tv / ev if ev else float("nan"),
        pv_fcfs=pv_fcfs,
        discount_factors=discount_factors,
    )


def wacc_sensitivity(
    fcfs: list[float],
    wacc_axis: list[float],
    g_axis: list[float],
    *,
    net_debt: float = 0.0,
    mid_year: bool = True,
) -> list[list[float]]:
    """Build a WACC × terminal-growth sensitivity grid of equity values."""
    grid = []
    for w in wacc_axis:
        row = []
        for g in g_axis:
            try:
                row.append(dcf(fcfs, w, g, net_debt=net_debt, mid_year=mid_year).equity_value)
            except ValueError:
                row.append(float("nan"))
        grid.append(row)
    return grid


# ---------------------------------------------------------------------------
# WACC build (CAPM)
# ---------------------------------------------------------------------------

def capm_cost_of_equity(rf: float, beta: float, erp: float) -> float:
    return rf + beta * erp


def wacc(
    cost_of_equity: float,
    *,
    cost_of_debt_pretax: float = 0.0,
    tax_rate: float = 0.21,
    debt_weight: float = 0.0,
) -> float:
    after_tax_kd = cost_of_debt_pretax * (1 - tax_rate)
    equity_weight = 1.0 - debt_weight
    return cost_of_equity * equity_weight + after_tax_kd * debt_weight


