def median_multiple(comp_rows: list[dict], multiple_field: str) -> float:
    vals = []
    for row in comp_rows:
        v = row.get(multiple_field, "")
        if v in ("", "n/a", "negative", None):
            continue
        try:
            vals.append(float(v))
        except ValueError:
            continue
    if not vals:
        raise ValueError(f"No usable values for {multiple_field}")
    return median(vals)


def implied_valuation(arr: float, multiple: float) -> float:
    return arr * multiple


# ---------------------------------------------------------------------------
# Probability-weighted return analysis
# ---------------------------------------------------------------------------

@dataclass
class Outcome:
    name: str
    probability: float
    exit_value_usd_m: float


def expected_value(outcomes: Iterable[Outcome], *, ownership_pct: float) -> dict:
    outcomes = list(outcomes)
    total_p = sum(o.probability for o in outcomes)
    if abs(total_p - 1.0) > 1e-6:
        raise ValueError(f"Probabilities must sum to 1.0; got {total_p}")
    ev = 0.0
    for o in outcomes:
        ev += o.probability * o.exit_value_usd_m * ownership_pct
    return {"ev_usd_m": ev, "outcomes": outcomes}


def irr_5y_from_multiple(initial_check_usd_m: float, exit_usd_m: float) -> float:
    """Compute 5-year IRR for a fixed exit value."""
    if initial_check_usd_m <= 0:
        return float("nan")
    if exit_usd_m <= 0:
        return -1.0
    return (exit_usd_m / initial_check_usd_m) ** (1 / 5) - 1


# ---------------------------------------------------------------------------
# Formatting helpers
# ---------------------------------------------------------------------------

def fmt_money(v: float, *, dollars: bool = True, decimals: int = 1) -> str:
    if v is None or (isinstance(v, float) and v != v):
        return "n/a"
    sign = "-" if v < 0 else ""
    av = abs(v)
    s = f"{sign}{'$' if dollars else ''}{av:,.{decimals}f}"
    return s


def fmt_pct(v: float, decimals: int = 1) -> str:
    if v is None or (isinstance(v, float) and v != v):
        return "n/a"
    return f"{v * 100:.{decimals}f}%"


def fmt_signed_pct(v: float, decimals: int = 1) -> str:
    if v is None or (isinstance(v, float) and v != v):
        return "n/a"
    return f"{v * 100:+.{decimals}f}%"
